import Foundation

extension App.DesignSystem {
    enum Padding {
        /// section: CGFloat = 20
        static let section: CGFloat = 20        // Between sections
        /// component: CGFloat = 12
        static let component: CGFloat = 12      // Inside components
        /// element: CGFloat = 8
        static let element: CGFloat = 8         // Between small elements
        /// tight: CGFloat = 4
        static let tight: CGFloat = 4           // Very close elements
        /// large: CGFloat = 16
        static let large: CGFloat = 16          // Large padding
        /// medium: CGFloat = 12
        static let medium: CGFloat = 12         // Medium padding

        // Screen margins
        /// screenHorizontal: CGFloat = 16
        static let screenHorizontal: CGFloat = 16
    }
}
